package co.suggesty.adapter;

public class Math {

//    두배 만들기
    public double twoTime(double num) {
        return num * 2;
    }

//    절반 만들기
    public double half(double num) {
        return num / 2;
    }

//    강화되었다고 가정할 알고리즘
    public Double doubled(Double d) {
        return d * 2;
    }


}
